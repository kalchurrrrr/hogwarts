class Gryffindor {
    private int bravery;
    private int chivalry;
    private int daring;
    public Gryffindor(int bravery, int chivalry, int daring) {
        this.bravery = bravery;
        this.chivalry = chivalry;
        this.daring = daring;
    }

    public int getBravery() {
        return bravery;
    }

    public int getChivalry() {
        return chivalry;
    }

    public int getDaring() {
        return daring;
    }
}